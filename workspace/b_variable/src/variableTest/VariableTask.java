package variableTest;

public class VariableTask {

	public static void main(String[] args) {
//		이름,나이,키,취미
//		4개의 변수를 선언하여 각 값으로 초기화한 뒤
//		모두 출력한다
//		이름은 문자열이므로 String 타입으로 선언
		String name = "조영인";
//		나이는 정수이므로 int 타입으로 선언
		int age= 26;
//		키는 실수이므로 double 타입으로 선언
		double height = 179.4;
//		취미는 문자열이므로 String 타입으로 선언
		String hobby = "게임";
//		선언한 4개의 변수를 출력한다 
		System.out.println(age);
		System.out.println(name);
		System.out.println(height);
		System.out.println(hobby);
		System.out.println();
//		과제 예제 
//		과자의 이름 수량과 가격 무게를 4개의 변수를 선언한후 출력하라
		String snackName="새우깡";
		int quantity = 3;
		int price=1700;
		double weight = 100.25;
		
		System.out.println(snackName);
		System.out.println(quantity);
		System.out.println(price);
		System.out.println(weight);
		
		
		
	
		

	}

}
